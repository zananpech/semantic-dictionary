export function searchMock(query: string, mode: 'semantic' | 'keyword'): WordEntry[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];

  if (mode === 'keyword') {
    return MOCK_WORDS.filter(w =>
      w.word.includes(q) || w.pos.includes(q)
    );
  }

  // Semantic: score by how many query words appear in definition/synonyms
  const queryTokens = q.split(/\s+/).filter(Boolean);
  const scored = MOCK_WORDS.map(entry => {
    const haystack = entry.senses
      .map(s => `${s.definition} ${s.synonyms.join(' ')} ${s.examples.join(' ')}`)
      .join(' ')
      .toLowerCase();
    const score = queryTokens.filter(t => haystack.includes(t)).length;
    return { entry, score };
  }).filter(r => r.score > 0);

  scored.sort((a, b) => b.score - a.score);
  return scored.map(r => r.entry);
}

export function getRandomWords(count = 5): WordEntry[] {
  const shuffled = [...MOCK_WORDS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
